package leetcode.addTwoNumbers_002;

/**
 * Definition for singly-linked list.
 *
 * @author mr.24
 * @version Id: ListNode, v 1.0 2021/3/20 下午1:26 Exp $$
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}